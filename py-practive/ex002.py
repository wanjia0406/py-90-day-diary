# 门槛（元）、对应税率、该段之前已得奖金
# 002 个税计算器
# 描述
# 企业发放的奖金根据利润 profit 分段提成：
# 表格
# 复制
# 利润区间（万元）	提成比例
# ≤10	10 %
# 10 ~ 20	超出 10 万部分 7.5 %
# 20 ~ 40	超出 20 万部分 5 %
# 40 ~ 60	超出 40 万部分 3 %
# 60 ~ 100	超出 60 万部分 1.5 %
# ＞100	超出 100 万部分 1 %
# 输入一个整数 profit（单位：元），输出应发放奖金总数（单位：元），保留 2 位小数。

TABLE = [
    (100_000, 0.10, 0),
    (200_000, 0.075, 10_000),
    (400_000, 0.05, 17_500),
    (600_000, 0.03, 27_500),
    (1_000_000, 0.015, 33_500),
    (float('inf'), 0.01, 39_500),
]

def bonus(profit: int) -> float:
    for top, rate, base in TABLE:
        if profit <= top:
            return base + (profit - (top - 100_000 if base else 0)) * rate

print(f"{bonus(int(input('请输入利润（元）：'))):.2f}")